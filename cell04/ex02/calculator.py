#!/usr/bin/env python3
first_num = int(input("Give me the first number: "))
sec_num = int(input("Give me the second number: "))
print("Thank you!")
addition = first_num + sec_num
print(f"{first_num} + {sec_num} = {addition}")
subtract = first_num - sec_num
print(f"{first_num} - {sec_num} = {subtract}")
division = first_num // sec_num
print(f"{first_num} / {sec_num} = {division}")
multiply = first_num * sec_num
print(f"{first_num} * {sec_num} = {multiply}")
